Definition join (x y:quadrivalent) : quadrivalent :=
  match x,y with
  | QTop, _ | _, QTop => QTop    
  | QBottom, a | a, QBottom => a
  | QTrue, QTrue => QTrue
  | QFalse, QFalse => QFalse
  | QTrue, QFalse | QFalse, QTrue => QTop
  end.
Lemma join_exact: binary_exact qv qv qv join (∪).
Proof.
