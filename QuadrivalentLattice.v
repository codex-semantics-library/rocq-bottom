Qed.

Definition meet (x y:quadrivalent) : quadrivalent :=
  match x,y with
  | QBottom, _ | _, QBottom => QBottom
  | QTop, a | a, QTop => a
  | QTrue, QTrue => QTrue
  | QFalse, QFalse => QFalse
  | QTrue, QFalse | QFalse, QTrue => QBottom
  end.
Lemma meet_exact: binary_exact qv qv qv meet (∩).
Proof.
  move=> q1 q2.
  rewrite /ExactlyRepresents. unfold_set.
  move: q1 q2.
  solve_with_autoreflect.
Qed.



